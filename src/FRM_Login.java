    void login()
    {   try
        {   st=con.createStatement();
            rs=st.executeQuery("SELECT * FROM USUARIO WHERE USUARIO='"+jTextField1.getText()+"' AND "
                + "CONTRASEÑA='"+jPasswordField1.getText()+"';");
            if(rs.next())            
            {   JOptionPane.showMessageDialog(this, "BIENVENIDO "+jTextField1.getText());
                dispose();
                MDI_Principal mdip=new MDI_Principal();
                mdip.setVisible(true);
            }
            else JOptionPane.showMessageDialog(this, "ERROR DE USUARIO Y CONTRASEÑA");
        }
        catch(SQLException e){  JOptionPane.showMessageDialog(this,"Error debido a : "+e.toString()); }
    }
    
